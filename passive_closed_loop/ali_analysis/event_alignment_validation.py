"""
Validate task event timing against raw voltage and dF/F frame timing.

This pipeline is for closed-loop two-photon sessions where:

* ``orientations_logger.csv`` contains Bonsai timestamps for START/END,
  StimStart/StimEnd, photodiode state changes, and wheel encoder values.
* ``orientations_orientations0.csv`` contains the stimulus parameters and IDs
  actually generated by Bonsai for the motor-dependent second block.
* ``*VoltageRecording*.csv`` contains the raw analog Prairie voltage inputs.
* ``raw_voltages.h5`` contains the processed/binarized voltage streams.
* ``qc_results/dff.h5`` contains dF/F traces.

The important design choice is that block 2 uses the Bonsai output CSV, not the
planned task CSV, because block 2 depends on the mouse behavior and Bonsai
generated the actual performed stimulus stream online.

The validator produces:

* a performed block-2 event table with stimulus parameters, logger start/end
  times, and nearest dF/F frame index;
* a trial-level analysis table with standard 0 deg trial starts and oddball
  trials for downstream dF/F alignment;
* high-level validation checks;
* dF/F length and imaging-voltage checks using the raw Prairie CSV Input 3
  scope-exposure channel as the primary frame source;
* raw Prairie voltage channel health;
* processed HDF5 voltage channel health as a secondary comparison;
* START/END alignment to voltage edges.

This does not claim that every stimulus has a hardware photodiode voltage pulse.
For this session, the physical photodiode input has only one pulse, so per-event
hardware-photodiode validation is flagged as unavailable/warning. Event-to-dF/F
alignment is therefore based on Bonsai logger event timestamps mapped to the
raw Prairie VoltageRecording CSV Input 3 scope-exposure train. The processed
HDF5 voltage channels are still summarized as a comparison, but they are not
the primary frame source when the raw CSV is available.
"""

