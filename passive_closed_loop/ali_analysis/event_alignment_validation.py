def write_markdown_report(
    path: Path,
    validation_checks: pd.DataFrame,
    dff_length_checks: pd.DataFrame,
    raw_h5_health: pd.DataFrame,
    physical_input_health: Optional[pd.DataFrame],
    event_table: pd.DataFrame,
    analysis_trial_table: pd.DataFrame,
) -> None:
    """Write a compact markdown report."""
    def markdown_table(df: pd.DataFrame) -> str:
        """Render a small DataFrame as a markdown table without tabulate."""
        if df.empty:
            return "_No rows._"
        str_df = df.astype(str)
        headers = list(str_df.columns)
        rows = str_df.values.tolist()
        lines = [
            "| " + " | ".join(headers) + " |",
            "| " + " | ".join(["---"] * len(headers)) + " |",
        ]
        for row in rows:
            lines.append("| " + " | ".join(str(value).replace("\n", " ") for value in row) + " |")
        return "\n".join(lines)

    lines = [
        "# Event Alignment Validation Report",
        "",
        "## Recommendation",
        "",
        "Use `orientations_orientations0.csv` plus `orientations_logger.csv` for performed block-2 events.",
        "Use the raw Prairie `*VoltageRecording*.csv` `Input 3` scope-exposure edges to map logger event times onto dF/F frame indices.",
        "Use processed `raw_voltages.h5` channels only as a secondary comparison unless the raw CSV is unavailable.",
        "For this session, physical photodiode voltage validation is only usable if the physical photodiode input has enough edges; see the checks table.",
        "",
        "## High-Level Checks",
        "",
        markdown_table(validation_checks),
        "",
        "## dF/F Length Checks",
        "",
        markdown_table(dff_length_checks),
        "",
        "## Raw HDF5 Voltage Health",
        "",
        markdown_table(raw_h5_health[["channel", "role", "rising_edges", "falling_edges", "edge_rate_hz", "status", "note"]]),
        "",
    ]
    if physical_input_health is not None:
        lines.extend(
            [
                "## Physical Voltage Input Health",
                "",
                markdown_table(physical_input_health[["input", "role", "threshold", "threshold_source", "rising_edges", "falling_edges", "status", "note"]]),
                "",
            ]
        )
    lines.extend(
        [
            "## Event Table Summary",
            "",
            f"Performed block-2 UUID-paired frame-update rows: {len(event_table):,}",
            f"Imaging frame source: {event_table['Imaging_Frame_Source'].iloc[0] if 'Imaging_Frame_Source' in event_table else 'not recorded'}",
            f"First event start: {event_table['Stim_Start_sec'].min():.6f} sec",
            f"Last event end: {event_table['Stim_End_sec'].max():.6f} sec",
            f"Nearest dF/F frame range: {int(event_table['Nearest_Dff_Frame'].min())} to {int(event_table['Nearest_Dff_Frame'].max())}",
            "",
            "## Trial-Level Analysis Table",
            "",
            "This table is intended for neural alignment. Each row is one stimulus-presentation trial.",
            "Contiguous standard frame-update rows are collapsed into one standard presentation; each motor oddball row is one presentation.",
            markdown_table(
                analysis_trial_table.groupby("Analysis_Condition", as_index=False)
                .agg(
                    trials=("Analysis_Trial", "size"),
                    source_update_rows=("Source_Update_Rows", "sum"),
                    first_start_sec=("Stim_Start_sec", "min"),
                    last_start_sec=("Stim_Start_sec", "max"),
                    selection_rule=("Selection_Rule", "first"),
                )
            )
            if not analysis_trial_table.empty
            else "_No analysis trials._",
            "",
        ]
    )
    path.write_text("\n".join(lines))


def run_event_alignment_validation(
    data_dir: str | Path,
    output_dir: str | Path | None = None,
    scan_voltage_csv_inputs: bool = True,
) -> EventAlignmentValidationResult:
    """Run event, voltage, and dF/F alignment validation.

    Parameters
    ----------
    data_dir
        Session data directory.
    output_dir
        Directory for validation outputs. Defaults to ``data_dir``.
    scan_voltage_csv_inputs
        If True, scan the large Prairie voltage CSV in chunks to validate the
        physical Input0-Input7 channels shown in the wiring diagram. This takes
        longer but is useful for checking the physical photodiode and encoder
        inputs. Raw HDF5 voltage checks are always run.
    """
    data_dir = Path(data_dir).expanduser().resolve()
    output_dir = data_dir if output_dir is None else Path(output_dir).expanduser().resolve()
    output_dir.mkdir(parents=True, exist_ok=True)

    bonsai_output = load_bonsai_output(data_dir / BONSAI_OUTPUT_CSV)
    logger, starts, ends, markers, photodiode = parse_logger(data_dir / LOGGER_CSV)
    dff_meta = load_dff_metadata(data_dir)

    raw_h5_health, h5_imaging_rising, h5_imaging_frame_times, h5_voltage_fs = summarize_raw_h5_voltage_channels(
        data_dir,
        dff_nframes=dff_meta["dff_nframes"],
    )

    with h5py.File(data_dir / RAW_VOLTAGES_H5, "r") as h5:
        t_sec, _ = load_voltage_time_seconds(h5)
        h5_voltage_duration_sec = float(t_sec[-1] - t_sec[0])

    physical_input_health = None
    physical_input_health_path = None
    csv_edge_times: dict[str, dict[str, np.ndarray]] = {}
    csv_voltage_fs = np.nan
    csv_voltage_duration_sec = np.nan
    voltage_csv = find_voltage_csv(data_dir)
    if scan_voltage_csv_inputs and voltage_csv is not None:
        physical_input_health, csv_edge_times, csv_voltage_fs, csv_voltage_duration_sec = scan_physical_voltage_inputs_with_edges(
            voltage_csv,
            logger_photodiode_count=len(photodiode),
            collect_edge_inputs=START_END_CSV_INPUTS,
        )
        physical_input_health_path = output_dir / PHYSICAL_INPUT_HEALTH_CSV
        physical_input_health.to_csv(physical_input_health_path, index=False)

    if PRIMARY_SCOPE_INPUT in csv_edge_times and len(csv_edge_times[PRIMARY_SCOPE_INPUT]["rising"]) > 0:
        imaging_frame_times = csv_edge_times[PRIMARY_SCOPE_INPUT]["rising"]
        voltage_fs = csv_voltage_fs
        voltage_duration_sec = csv_voltage_duration_sec
        imaging_frame_source = f"VoltageRecording CSV {PRIMARY_SCOPE_INPUT.strip()} ({voltage_csv.name})"
        start_end_edge_times = csv_edge_times
        start_end_voltage_source = f"VoltageRecording CSV ({voltage_csv.name})"
    else:
        imaging_frame_times = h5_imaging_frame_times
        voltage_fs = h5_voltage_fs
        voltage_duration_sec = h5_voltage_duration_sec
        imaging_frame_source = "processed HDF5 raw/vol_img fallback"
        start_end_edge_times = h5_edge_times_for_start_end(data_dir)
        start_end_voltage_source = "processed HDF5 raw_voltages.h5 fallback"

    if len(imaging_frame_times) == 0:
        raise RuntimeError(
            "Could not detect imaging frame edges from raw VoltageRecording CSV Input 3 "
            "or processed HDF5 raw/vol_img fallback"
        )

    event_table = build_performed_event_table(
        bonsai_output=bonsai_output,
        starts=starts,
        ends=ends,
        imaging_frame_times=imaging_frame_times,
        dff_nframes=dff_meta["dff_nframes"],
        imaging_frame_source=imaging_frame_source,
    )
    event_table = add_duration_qc(event_table)
    analysis_trial_table = build_analysis_trial_table(event_table)
    stim_duration_summary, stim_duration_outliers = summarize_stim_durations(event_table)

    dff_length_checks = make_dff_length_checks(
        dff_meta=dff_meta,
        imaging_frame_times=imaging_frame_times,
        voltage_fs=voltage_fs,
        voltage_duration_sec=voltage_duration_sec,
        imaging_frame_source=imaging_frame_source,
    )
    start_end_alignment = align_start_end_to_voltage(
        markers,
        voltage_edge_times=start_end_edge_times,
        voltage_source=start_end_voltage_source,
    )

    validation_checks = make_validation_checks(
        bonsai_output=bonsai_output,
        starts=starts,
        ends=ends,
        events=event_table,
        markers=markers,
        photodiode=photodiode,
        dff_length_checks=dff_length_checks,
        start_end_alignment=start_end_alignment,
        physical_input_health=physical_input_health,
        imaging_frame_source=imaging_frame_source,
        analysis_trial_table=analysis_trial_table,
    )

    event_table_path = output_dir / EVENT_TABLE_CSV
    analysis_trial_table_path = output_dir / ANALYSIS_TRIAL_CSV
    validation_checks_path = output_dir / VALIDATION_CHECKS_CSV
    dff_length_path = output_dir / DFF_LENGTH_CSV
    raw_h5_health_path = output_dir / RAW_H5_HEALTH_CSV
    start_end_alignment_path = output_dir / START_END_ALIGNMENT_CSV
    stim_duration_summary_path = output_dir / STIM_DURATION_CSV
    stim_duration_outliers_path = output_dir / STIM_DURATION_OUTLIERS_CSV
    report_path = output_dir / REPORT_MD

    event_table.to_csv(event_table_path, index=False)
    analysis_trial_table.to_csv(analysis_trial_table_path, index=False)
    validation_checks.to_csv(validation_checks_path, index=False)
    dff_length_checks.to_csv(dff_length_path, index=False)
    raw_h5_health.to_csv(raw_h5_health_path, index=False)
    start_end_alignment.to_csv(start_end_alignment_path, index=False)
    stim_duration_summary.to_csv(stim_duration_summary_path, index=False)
    stim_duration_outliers.to_csv(stim_duration_outliers_path, index=False)
    write_markdown_report(
        report_path,
        validation_checks=validation_checks,
        dff_length_checks=dff_length_checks,
        raw_h5_health=raw_h5_health,
        physical_input_health=physical_input_health,
        event_table=event_table,
        analysis_trial_table=analysis_trial_table,
    )

    return EventAlignmentValidationResult(
        output_dir=output_dir,
        event_table_path=event_table_path,
        analysis_trial_table_path=analysis_trial_table_path,
        validation_checks_path=validation_checks_path,
        dff_length_path=dff_length_path,
        raw_h5_health_path=raw_h5_health_path,
        physical_input_health_path=physical_input_health_path,
        start_end_alignment_path=start_end_alignment_path,
        stim_duration_summary_path=stim_duration_summary_path,
        stim_duration_outliers_path=stim_duration_outliers_path,
        report_path=report_path,
        event_table=event_table,
        analysis_trial_table=analysis_trial_table,
        validation_checks=validation_checks,
        dff_length_checks=dff_length_checks,
        raw_h5_health=raw_h5_health,
        physical_input_health=physical_input_health,
        start_end_alignment=start_end_alignment,
        stim_duration_summary=stim_duration_summary,
        stim_duration_outliers=stim_duration_outliers,
    )


def build_arg_parser() -> argparse.ArgumentParser:
    """Build CLI parser."""
    parser = argparse.ArgumentParser(description="Validate event alignment, voltage health, and dF/F frame mapping.")
    parser.add_argument("--data-dir", required=True, help="Session data directory.")
    parser.add_argument("--output-dir", default=None, help="Output directory. Defaults to data-dir.")
    parser.add_argument(
        "--skip-voltage-csv-scan",
        action="store_true",
        help="Skip chunked scan of the large physical voltage CSV inputs.",
    )
    return parser


def main() -> None:
    """Command-line entry point."""
    args = build_arg_parser().parse_args()
    result = run_event_alignment_validation(
        data_dir=args.data_dir,
        output_dir=args.output_dir,
        scan_voltage_csv_inputs=not args.skip_voltage_csv_scan,
    )
    print(f"Saved event table: {result.event_table_path}")
    print(f"Saved trial-level analysis table: {result.analysis_trial_table_path}")
    print(f"Saved validation checks: {result.validation_checks_path}")
    print(f"Saved report: {result.report_path}")
    print("\nHigh-level checks:")
    print(result.validation_checks.to_string(index=False))


if __name__ == "__main__":
    main()
