def infer_ttl_threshold(min_value: float, max_value: float, fallback: Optional[float]) -> tuple[float, str]:
    """Infer a TTL threshold from raw analog voltage range."""
    if np.isfinite(min_value) and np.isfinite(max_value) and (max_value - min_value) > 0.5:
        return float(min_value + 0.5 * (max_value - min_value)), "adaptive_midrange"
    if fallback is None:
        return np.nan, "not_applicable"
    return float(fallback), "fallback"


def collect_voltage_csv_stats(
    voltage_csv: Path,
    chunksize: int,
) -> tuple[dict[str, dict], float, float, float]:
    """First pass over the raw Prairie CSV to collect stats and sample rate."""
    usecols = ["Time(ms)"] + list(PHYSICAL_INPUTS.keys())
    states = {
        col: {"min": np.inf, "max": -np.inf, "sum": 0.0, "sum2": 0.0, "n": 0}
        for col in PHYSICAL_INPUTS
    }
    first_time = np.nan
    last_time = np.nan
    first_dt = np.nan
    for chunk in pd.read_csv(voltage_csv, usecols=usecols, chunksize=chunksize):
        t_sec = chunk["Time(ms)"].to_numpy(dtype=float) / 1000.0
        if np.isnan(first_time):
            first_time = float(t_sec[0])
            first_dt = float(np.median(np.diff(t_sec[: min(10000, len(t_sec))])))
        last_time = float(t_sec[-1])

        for col in PHYSICAL_INPUTS:
            x = chunk[col].to_numpy(dtype=float)
            state = states[col]
            state["min"] = min(state["min"], float(np.nanmin(x)))
            state["max"] = max(state["max"], float(np.nanmax(x)))
            state["sum"] += float(np.nansum(x))
            state["sum2"] += float(np.nansum(x * x))
            state["n"] += len(x)

    voltage_fs = 1.0 / first_dt if np.isfinite(first_dt) and first_dt > 0 else np.nan
    return states, first_time, last_time, voltage_fs


def scan_physical_voltage_inputs_with_edges(
    voltage_csv: Path,
    logger_photodiode_count: int,
    chunksize: int = 500_000,
    collect_edge_inputs: Optional[list[str]] = None,
) -> tuple[pd.DataFrame, dict[str, dict[str, np.ndarray]], float, float]:
    """Scan raw Prairie CSV, summarize inputs, and collect selected edge times."""
    collect_edge_inputs = collect_edge_inputs or []
    stats, first_time, last_time, voltage_fs = collect_voltage_csv_stats(voltage_csv, chunksize)
    usecols = ["Time(ms)"] + list(PHYSICAL_INPUTS.keys())

    states = {}
    for col, spec in PHYSICAL_INPUTS.items():
        threshold, threshold_source = infer_ttl_threshold(stats[col]["min"], stats[col]["max"], spec["threshold"])
        states[col] = {
            **stats[col],
            "threshold": threshold,
            "threshold_source": threshold_source,
            "rising_edges": 0,
            "falling_edges": 0,
            "high_count": 0,
            "first_rising_sec": np.nan,
            "last_rising_sec": np.nan,
            "prev_digital": None,
        }

    edge_times = {
        col: {"rising": [], "falling": []}
        for col in collect_edge_inputs
    }

    for chunk in pd.read_csv(voltage_csv, usecols=usecols, chunksize=chunksize):
        t_sec = chunk["Time(ms)"].to_numpy(dtype=float) / 1000.0
        for col, spec in PHYSICAL_INPUTS.items():
            if spec["kind"] == "analog":
                continue

            state = states[col]
            threshold = float(state["threshold"])
            x = chunk[col].to_numpy(dtype=float)
            digital = x > threshold
            state["high_count"] += int(digital.sum())

            boundary_rising = state["prev_digital"] is not None and (not state["prev_digital"]) and bool(digital[0])
            boundary_falling = state["prev_digital"] is not None and state["prev_digital"] and (not bool(digital[0]))
            if boundary_rising:
                state["rising_edges"] += 1
                state["first_rising_sec"] = (
                    float(t_sec[0]) if np.isnan(state["first_rising_sec"]) else state["first_rising_sec"]
                )
                state["last_rising_sec"] = float(t_sec[0])
                if col in edge_times:
                    edge_times[col]["rising"].append(np.array([float(t_sec[0])], dtype=float))
            if boundary_falling:
                state["falling_edges"] += 1
                if col in edge_times:
                    edge_times[col]["falling"].append(np.array([float(t_sec[0])], dtype=float))

            rising = np.flatnonzero((~digital[:-1]) & digital[1:]) + 1
            falling = np.flatnonzero(digital[:-1] & (~digital[1:])) + 1
            state["rising_edges"] += int(len(rising))
            state["falling_edges"] += int(len(falling))
            if len(rising):
                state["first_rising_sec"] = (
                    float(t_sec[rising[0]]) if np.isnan(state["first_rising_sec"]) else state["first_rising_sec"]
                )
                state["last_rising_sec"] = float(t_sec[rising[-1]])
            if col in edge_times:
                if len(rising):
                    edge_times[col]["rising"].append(t_sec[rising].astype(float, copy=True))
                if len(falling):
                    edge_times[col]["falling"].append(t_sec[falling].astype(float, copy=True))
            state["prev_digital"] = bool(digital[-1])

    duration_sec = last_time - first_time
    rows = []
    for col, spec in PHYSICAL_INPUTS.items():
        state = states[col]
        mean = state["sum"] / state["n"]
        std = max(state["sum2"] / state["n"] - mean * mean, 0) ** 0.5
        row = {
            "input": col.strip(),
            "role": spec["role"],
            "kind": spec["kind"],
            "threshold": state["threshold"],
            "threshold_source": state["threshold_source"],
            "min": state["min"],
            "mean": mean,
            "max": state["max"],
            "std": std,
            "rising_edges": state["rising_edges"],
            "falling_edges": state["falling_edges"],
            "high_fraction": state["high_count"] / state["n"] if spec["kind"] != "analog" else np.nan,
            "first_rising_sec": state["first_rising_sec"],
            "last_rising_sec": state["last_rising_sec"],
            "status": "OK",
            "note": "",
        }

        if spec["kind"] == "analog":
            row["status"] = "OK" if std > 1e-4 else "WARN"
            row["note"] = "analog input active" if row["status"] == "OK" else "analog input flat"
        elif col == " Input 1":
            enough = state["rising_edges"] + state["falling_edges"] >= max(10, 0.5 * logger_photodiode_count)
            row["status"] = "OK" if enough else "WARN"
            row["note"] = (
                "physical photodiode input has enough transitions"
                if enough
                else f"physical photodiode has too few transitions for per-event validation; logger photodiode events={logger_photodiode_count}"
            )
        elif col == PRIMARY_SCOPE_INPUT:
            row["status"] = "OK" if state["rising_edges"] > 100 else "WARN"
            row["note"] = "raw CSV scope exposure active; primary frame-alignment source" if row["status"] == "OK" else "scope exposure has few/no edges"
        elif col in [" Input 6", " Input 7"]:
            row["status"] = "OK" if state["rising_edges"] > 100 else "WARN"
            row["note"] = "encoder phase active" if row["status"] == "OK" else "encoder phase has few/no edges"
        elif col == " Input 0":
            row["status"] = "INFO" if state["rising_edges"] == 0 else "OK"
            row["note"] = "trial sync input unused/flat" if state["rising_edges"] == 0 else "trial sync has edges"
        else:
            row["status"] = "OK" if state["rising_edges"] > 0 else "WARN"
            row["note"] = "input has edges" if row["status"] == "OK" else "input has no edges"

        rows.append(row)

    for col in edge_times:
        for edge_name in ["rising", "falling"]:
            parts = edge_times[col][edge_name]
            edge_times[col][edge_name] = np.concatenate(parts) if parts else np.array([], dtype=float)

    out = pd.DataFrame(rows)
    out["recording_duration_sec"] = duration_sec
    out["voltage_fs_hz"] = voltage_fs
    return out, edge_times, voltage_fs, duration_sec


def scan_physical_voltage_inputs(
    voltage_csv: Path,
    logger_photodiode_count: int,
    chunksize: int = 500_000,
) -> pd.DataFrame:
    """Scan the large Prairie CSV in chunks and summarize physical input health."""
