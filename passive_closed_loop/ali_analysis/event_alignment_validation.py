def status(ok: bool, warn: bool = False) -> str:
    """Return OK/WARN/FAIL status."""
    if ok:
        return "OK"
    return "WARN" if warn else "FAIL"


def load_voltage_time_seconds(h5: h5py.File) -> tuple[np.ndarray, float]:
    """Load voltage time as seconds and return the inferred sample rate."""
    t_raw = np.asarray(h5["raw/vol_time"])
    dt = float(np.median(np.diff(t_raw[: min(10000, len(t_raw))])))
    # Prairie voltage time here is in ms. Use this heuristic to keep the
    # function robust for files already stored in seconds.
    time_scale = 0.001 if dt > 0.01 else 1.0
    t_sec = t_raw * time_scale
    fs = 1.0 / float(np.median(np.diff(t_sec[: min(10000, len(t_sec))])))
    return t_sec, fs


def detect_edges(signal: np.ndarray, threshold: float) -> tuple[np.ndarray, np.ndarray, np.ndarray]:
    """Return digital state, rising edge indices, and falling edge indices."""
    digital = signal > threshold
    rising = np.flatnonzero((~digital[:-1]) & digital[1:]) + 1
    falling = np.flatnonzero(digital[:-1] & (~digital[1:])) + 1
    return digital, rising, falling


def nearest_indices(sorted_values: np.ndarray, query_values: np.ndarray) -> tuple[np.ndarray, np.ndarray]:
    """Find nearest indices in a sorted 1D array for each query value."""
    idx = np.searchsorted(sorted_values, query_values)
    idx0 = np.clip(idx - 1, 0, len(sorted_values) - 1)
    idx1 = np.clip(idx, 0, len(sorted_values) - 1)
    choose_right = np.abs(sorted_values[idx1] - query_values) < np.abs(sorted_values[idx0] - query_values)
    nearest = np.where(choose_right, idx1, idx0)
    deltas = query_values - sorted_values[nearest]
    return nearest, deltas


def load_bonsai_output(path: Path) -> pd.DataFrame:
    """Load Bonsai output stimulus parameters with canonical column names."""
    df = pd.read_csv(path).rename(columns=RENAME_TO_CANONICAL)
    required = ["Id", "Trial_Number", "Trial_Type", "Block_Number", "Block_Type", "Duration"]
    missing = [col for col in required if col not in df.columns]
    if missing:
        raise ValueError(f"Bonsai output CSV is missing columns: {missing}")
    return df


def parse_logger(path: Path) -> tuple[pd.DataFrame, pd.DataFrame, pd.DataFrame, pd.DataFrame]:
    """Load logger and extract StimStart/StimEnd/marker tables."""
    logger = pd.read_csv(path)
    values = logger["Value"].astype(str)

    starts = logger.loc[values.str.startswith("StimStart-"), ["Frame", "Timestamp", "Value"]].copy()
    starts["Id"] = starts["Value"].str.replace("StimStart-", "", regex=False)
    starts = starts.rename(columns={"Frame": "Logger_Start_Frame", "Timestamp": "Stim_Start_sec"})

    ends = logger.loc[values.str.startswith("StimEnd-"), ["Frame", "Timestamp", "Value"]].copy()
    ends["Id"] = ends["Value"].str.replace("StimEnd-", "", regex=False)
    ends = ends.rename(columns={"Frame": "Logger_End_Frame", "Timestamp": "Stim_End_sec"})

    markers = logger.loc[values.isin(["START", "END", "EndFrame"]), ["Frame", "Timestamp", "Value"]].copy()
    photodiode = logger.loc[values.isin(["Photodiode-0", "Photodiode-1"]), ["Frame", "Timestamp", "Value"]].copy()
    return logger, starts, ends, markers, photodiode


def build_performed_event_table(
    bonsai_output: pd.DataFrame,
    starts: pd.DataFrame,
    ends: pd.DataFrame,
    imaging_frame_times: np.ndarray,
    dff_nframes: int,
    imaging_frame_source: str,
) -> pd.DataFrame:
    """Merge Bonsai output rows with logger timing and nearest dF/F frame."""
    events = bonsai_output.merge(
        starts[["Id", "Logger_Start_Frame", "Stim_Start_sec"]],
        on="Id",
        how="left",
        validate="one_to_one",
    ).merge(
        ends[["Id", "Logger_End_Frame", "Stim_End_sec"]],
        on="Id",
        how="left",
        validate="one_to_one",
    )
    events["Logger_Duration_sec"] = events["Stim_End_sec"] - events["Stim_Start_sec"]

    usable_frame_times = imaging_frame_times[:dff_nframes]
    nearest_frame, delta = nearest_indices(usable_frame_times, events["Stim_Start_sec"].to_numpy())
    events["Nearest_Dff_Frame"] = nearest_frame
    events["Nearest_Dff_Time_sec"] = usable_frame_times[nearest_frame]
    events["Stim_To_Dff_Frame_Delta_sec"] = delta
    events["Nearest_Dff_Frame_In_Range"] = (events["Nearest_Dff_Frame"] >= 0) & (events["Nearest_Dff_Frame"] < dff_nframes)
    events["Imaging_Frame_Source"] = imaging_frame_source
    events["Imaging_Frame_Edge_Count"] = int(len(imaging_frame_times))
    return events


def add_duration_qc(events: pd.DataFrame, tolerance_sec: float = 0.020) -> pd.DataFrame:
    """Add logger-vs-CSV duration error and outlier columns."""
    events = events.copy()
    events["Duration_Error_sec"] = events["Logger_Duration_sec"] - pd.to_numeric(events["Duration"], errors="coerce")
    events["Duration_Outlier"] = events["Duration_Error_sec"].abs() > tolerance_sec
    return events


def summarize_stim_durations(events: pd.DataFrame, tolerance_sec: float = 0.020) -> tuple[pd.DataFrame, pd.DataFrame]:
    """Summarize logger stimulus durations and identify duration outliers."""
    if "Duration_Outlier" not in events or "Duration_Error_sec" not in events:
        events = add_duration_qc(events, tolerance_sec=tolerance_sec)

    summary = (
        events.groupby("Trial_Type", dropna=False)
        .agg(
            count=("Id", "size"),
            expected_duration_sec=("Duration", "median"),
            logger_duration_median_sec=("Logger_Duration_sec", "median"),
            logger_duration_min_sec=("Logger_Duration_sec", "min"),
            logger_duration_max_sec=("Logger_Duration_sec", "max"),
            median_error_sec=("Duration_Error_sec", "median"),
            max_abs_error_sec=("Duration_Error_sec", lambda x: x.abs().max()),
            outliers=("Duration_Outlier", "sum"),
        )
        .reset_index()
    )
    outliers = events.loc[
        events["Duration_Outlier"],
        [
            "Trial_Number",
            "Trial_Type",
            "Id",
            "Duration",
            "Logger_Duration_sec",
            "Duration_Error_sec",
            "Stim_Start_sec",
            "Stim_End_sec",
        ],
    ].copy()
    return summary, outliers


def orientation_to_degrees(events: pd.DataFrame) -> pd.Series:
    """Return orientation in degrees, accepting radians or degrees."""
    orientation = pd.to_numeric(events["Orientation"], errors="coerce")
    finite = orientation.dropna().abs()
    if not finite.empty and finite.max() <= 2 * np.pi + 1e-6:
        return np.rad2deg(orientation)
    return orientation


def nearest_distance_to_times(query: np.ndarray, reference: np.ndarray) -> np.ndarray:
    """Return nearest absolute distance from each query time to reference times."""
    if len(reference) == 0:
        return np.full(len(query), np.inf, dtype=float)
    reference = np.sort(np.asarray(reference, dtype=float))
    idx = np.searchsorted(reference, query)
    left = reference[np.clip(idx - 1, 0, len(reference) - 1)]
    right = reference[np.clip(idx, 0, len(reference) - 1)]
    return np.minimum(np.abs(query - left), np.abs(query - right))


def evenly_spaced_rows(df: pd.DataFrame, n_rows: int) -> pd.DataFrame:
    """Select deterministic evenly spaced rows from a sorted DataFrame."""
    if n_rows <= 0 or df.empty:
        return df.iloc[0:0].copy()
    if n_rows >= len(df):
        return df.copy()
    positions = np.linspace(0, len(df) - 1, n_rows)
    indices = np.unique(np.round(positions).astype(int))
    if len(indices) < n_rows:
        remaining = np.setdiff1d(np.arange(len(df)), indices, assume_unique=False)
        indices = np.sort(np.concatenate([indices, remaining[: n_rows - len(indices)]]))
    return df.iloc[indices[:n_rows]].copy()


def standard_trial_starts_between_oddballs(events: pd.DataFrame) -> pd.DataFrame:
    """Return first standard 0 deg start in each interval between oddballs.

    The Bonsai output has one row per short visual presentation. For standard
    0 deg trial alignment, a trial is treated as the start of a standard bout
    bounded by two consecutive motor-oddball rows. Each selected row is the
    first valid standard ``StimStart`` after the previous oddball and before
    the next oddball.
    """
    events = events.sort_values("Stim_Start_sec").reset_index(drop=True).copy()
    if "Duration_Outlier" in events:
        events = events[~events["Duration_Outlier"]].reset_index(drop=True)
    if "Orientation_Mod180" not in events:
        events["Orientation_Deg"] = orientation_to_degrees(events).round(6)
        events["Orientation_Mod180"] = np.mod(events["Orientation_Deg"], 180)

    oddballs = events[events["Trial_Type"].isin(ANALYSIS_CONDITION_BY_TRIAL_TYPE)].reset_index(drop=True)
    rows = []
    for oddball_index in range(len(oddballs) - 1):
        previous_oddball = oddballs.iloc[oddball_index]
        next_oddball = oddballs.iloc[oddball_index + 1]
        in_interval = (
            (events["Stim_Start_sec"] > previous_oddball["Stim_Start_sec"])
            & (events["Stim_Start_sec"] < next_oddball["Stim_Start_sec"])
            & events["Trial_Type"].eq("standard")
            & np.isclose(events["Orientation_Mod180"], 0.0, atol=1e-4)
        )
        candidates = events.loc[in_interval].sort_values("Stim_Start_sec")
        if candidates.empty:
            continue
        row = candidates.iloc[0].copy()
        row["Previous_Oddball_Trial_Type"] = previous_oddball["Trial_Type"]
        row["Previous_Oddball_Start_sec"] = previous_oddball["Stim_Start_sec"]
        row["Next_Oddball_Trial_Type"] = next_oddball["Trial_Type"]
        row["Next_Oddball_Start_sec"] = next_oddball["Stim_Start_sec"]
        row["Inter_Oddball_Interval_sec"] = next_oddball["Stim_Start_sec"] - previous_oddball["Stim_Start_sec"]
        rows.append(row)

    if not rows:
        return events.iloc[0:0].copy()
    out = pd.DataFrame(rows).reset_index(drop=True)
    out["Standard_Trial_Definition"] = "first standard 0 deg StimStart between consecutive oddballs"
    return out


def build_analysis_trial_table(
    events: pd.DataFrame,
    standard_exclusion_sec: float = 1.0,
) -> pd.DataFrame:
    """Build the trial-level table for neural alignment.

    The Bonsai output contains one UUID-paired ``StimStart``/``StimEnd`` row
    per visual frame update during standard closed-loop visual flow. A trial
    for neural analysis is a stimulus presentation, not one frame update:

    * contiguous standard frame-update rows are collapsed into one standard
      presentation;
    * each non-standard motor oddball row is kept as its own presentation.
    """
    events = events.copy()
    if "Duration_Outlier" not in events:
        events = add_duration_qc(events)
    events["Orientation_Deg"] = orientation_to_degrees(events).round(6)
    events["Orientation_Mod180"] = np.mod(events["Orientation_Deg"], 180)
    events["Source_Event_Row"] = np.arange(len(events), dtype=int)

    valid = events[
        events["Stim_Start_sec"].notna()
        & events["Stim_End_sec"].notna()
        & events["Id"].notna()
    ].sort_values("Stim_Start_sec").reset_index(drop=True).copy()
    if valid.empty:
        return valid

    trial_type = valid["Trial_Type"].astype(str)
    new_presentation = trial_type.ne(trial_type.shift()) | trial_type.ne("standard")
    valid["Presentation_Run"] = new_presentation.cumsum()

    first_cols = [
        "Contrast",
        "Delay",
        "DiameterX",
        "DiameterY",
        "Orientation",
        "Spatial_Frequency",
        "Temporal_Frequency",
        "X",
        "Y",
        "Phase",
        "Trial_Type",
        "Block_Type",
        "Block_Number",
        "Block_Label",
        "Sequence_Number",
        "Trial_In_Sequence",
        "Stim_Start_sec",
        "Logger_Start_Frame",
        "Nearest_Dff_Frame",
        "Nearest_Dff_Time_sec",
        "Stim_To_Dff_Frame_Delta_sec",
        "Nearest_Dff_Frame_In_Range",
        "Imaging_Frame_Source",
        "Imaging_Frame_Edge_Count",
        "Orientation_Deg",
        "Orientation_Mod180",
        "Source_Event_Row",
    ]
    agg = {col: (col, "first") for col in first_cols if col in valid.columns}
    agg.update(
        {
            "Id": ("Id", "first"),
            "Presentation_Start_Id": ("Id", "first"),
            "Presentation_End_Id": ("Id", "last"),
            "Trial_Number": ("Trial_Number", "first"),
            "Presentation_End_Trial_Number": ("Trial_Number", "last"),
            "Stim_End_sec": ("Stim_End_sec", "last"),
            "Logger_End_Frame": ("Logger_End_Frame", "last"),
            "Duration": ("Duration", "sum"),
            "Logger_Duration_sec": ("Logger_Duration_sec", "sum"),
            "Duration_Error_sec": ("Duration_Error_sec", "sum"),
            "Duration_Outlier": ("Duration_Outlier", "any"),
            "Source_Update_Rows": ("Id", "size"),
            "Source_Event_End_Row": ("Source_Event_Row", "last"),
        }
    )

    analysis = valid.groupby("Presentation_Run", as_index=False).agg(**agg)
    if analysis.empty:
        return analysis

    analysis["Analysis_Condition"] = analysis["Trial_Type"].map(ANALYSIS_CONDITION_BY_TRIAL_TYPE)
    analysis.loc[
        analysis["Analysis_Condition"].isna()
        & analysis["Trial_Type"].eq("standard")
        & np.isclose(analysis["Orientation_Mod180"], 0.0, atol=1e-4),
        "Analysis_Condition",
    ] = "standard_control"
    analysis["Analysis_Condition"] = analysis["Analysis_Condition"].fillna(analysis["Trial_Type"].astype(str))
    analysis["Selection_Rule"] = np.where(
        analysis["Trial_Type"].eq("standard"),
        "contiguous standard frame-update presentation",
        "single motor-oddball StimStart/StimEnd presentation",
    )

    condition_order = {
        "standard_control": 0,
        "omission": 1,
        "halt": 2,
        "orientation_90": 3,
        "orientation_45": 4,
    }
    analysis["Analysis_Condition_Order"] = analysis["Analysis_Condition"].map(condition_order).fillna(99).astype(int)
    analysis = analysis.sort_values(["Stim_Start_sec", "Analysis_Condition_Order"]).reset_index(drop=True)
    analysis["Analysis_Trial"] = np.arange(1, len(analysis) + 1, dtype=int)
    analysis["Analysis_Trial_In_Condition"] = analysis.groupby("Analysis_Condition").cumcount() + 1
    analysis["Is_Analysis_Trial"] = True

    front_cols = [
        "Analysis_Trial",
        "Analysis_Trial_In_Condition",
        "Analysis_Condition",
        "Analysis_Condition_Order",
        "Selection_Rule",
        "Is_Analysis_Trial",
        "Source_Update_Rows",
        "Source_Event_Row",
        "Source_Event_End_Row",
        "Presentation_Start_Id",
        "Presentation_End_Id",
    ]
    remaining = [col for col in analysis.columns if col not in front_cols]
    return analysis[front_cols + remaining]


def summarize_raw_h5_voltage_channels(
    data_dir: Path,
    dff_nframes: int,
) -> tuple[pd.DataFrame, np.ndarray, np.ndarray, float]:
    """Summarize raw HDF5 voltage channels and return imaging frame times."""
    rows = []
    raw_path = data_dir / RAW_VOLTAGES_H5
    with h5py.File(raw_path, "r") as h5:
        t_sec, voltage_fs = load_voltage_time_seconds(h5)
        imaging_frame_times = np.array([], dtype=float)
        imaging_rising = np.array([], dtype=int)

        for key, spec in RAW_H5_CHANNELS.items():
            if key not in h5:
                rows.append({"channel": key, "role": spec["role"], "status": "FAIL", "note": "missing"})
                continue

            x = np.asarray(h5[key])
            row = {
                "channel": key,
                "role": spec["role"],
                "kind": spec["kind"],
                "n_samples": int(len(x)),
                "min": float(np.nanmin(x)),
                "mean": float(np.nanmean(x)),
                "max": float(np.nanmax(x)),
                "std": float(np.nanstd(x)),
                "threshold": spec["threshold"],
                "rising_edges": np.nan,
                "falling_edges": np.nan,
                "median_period_sec": np.nan,
                "edge_rate_hz": np.nan,
                "high_fraction": np.nan,
                "status": "OK",
                "note": "",
            }

            if spec["kind"] == "analog":
                row["status"] = "OK" if row["std"] > 1e-4 else "WARN"
                row["note"] = "analog waveform active" if row["status"] == "OK" else "analog waveform appears flat"
            else:
                threshold = float(spec["threshold"])
                digital, rising, falling = detect_edges(x, threshold)
                row["rising_edges"] = int(len(rising))
                row["falling_edges"] = int(len(falling))
                row["high_fraction"] = float(digital.mean())
                if len(rising) > 2:
                    periods = np.diff(t_sec[rising])
                    row["median_period_sec"] = float(np.median(periods))
                    row["edge_rate_hz"] = float(1.0 / np.median(periods))

                if key == "raw/vol_img":
                    imaging_rising = rising
                    imaging_frame_times = t_sec[rising]
                    diff = abs(len(rising) - dff_nframes)
                    row["status"] = "OK" if diff <= 5 else "WARN"
                    row["note"] = f"scope exposure edges minus dF/F frames = {len(rising) - dff_nframes:+d}"
                elif spec["kind"] == "ttl_train":
                    row["status"] = "OK" if len(rising) > 100 else "WARN"
                    row["note"] = "active TTL train" if row["status"] == "OK" else "few/no TTL edges"
                elif spec["kind"] == "ttl_events":
                    row["status"] = "OK" if len(rising) > 0 else "WARN"
                    row["note"] = "event TTL active" if row["status"] == "OK" else "no event TTL edges"
                else:
                    row["status"] = "INFO" if len(rising) == 0 else "OK"
                    row["note"] = "optional channel unused/flat" if len(rising) == 0 else "optional channel has edges"

            rows.append(row)

